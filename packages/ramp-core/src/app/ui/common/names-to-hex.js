const nameToHex = name => {

    // find and replace all accents on the word
    const rExps = [
        { re: /[\xC0-\xC5]/g, ch: 'A' },
        { re: /[\xC6]/g, ch: 'AE' },
        { re: /[\xE0-\xE5]/g, ch: 'a' },
        { re: /[\xE6]/g, ch: 'ae' },
        { re: /[\xC7]/g, ch: 'C' },
        { re: /[\xE7]/g, ch: 'c' },
        { re: /[\xC8-\xCB]/g, ch: 'E' },
        { re: /[\xE8-\xEB]/g, ch: 'e' },
        { re: /[\xCC-\xCF]/g, ch: 'I' },
        { re: /[\xEC-\xEF]/g, ch: 'i' },
        { re: /[\xD1]/g, ch: 'N' },
        { re: /[\xF1]/g, ch: 'n' },
        { re: /[\xD2-\xD6]/g, ch: 'O' },
        { re: /[\xF2-\xF6]/g, ch: 'o' },
        { re: new RegExp(/œ/g), ch: 'oe'},
        { re: /[\xD9-\xDC]/g, ch: 'U' },
        { re: /[\xF9-\xFC]/g, ch: 'u' },
        { re: /[\xDD]/g, ch: 'Y' },
        { re: /[\xFD,\xFF]/g, ch: 'y' }
    ];

    for (let i = 0; i < rExps.length; i++) {
        name = name.replace(rExps[i].re, rExps[i].ch);
    }

    return names[name.toLowerCase()] ? names[name.toLowerCase()] : 'ffffff';
}

